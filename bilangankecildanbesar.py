print('Aplikasi mencari bilangan terbesar dan terkecil')
print('===============================================')
bil1 = int(input("Masukan Bilangan 1 = "))
bil2 = int(input("Masukan Bilangan 2 = "))

# NilaiTerbesar
if (bil1 > bil2):
    print("Bilangan Terbesar adalah = ", bil1)
else:
    print("Bilangan Terbesar adalah = ", bil2)

# NilaiTerkecil

if (bil1 < bil2):
    print("Bilangan Terkecil adalah = ", bil1)
else:
    print("Bilangan Terkecil adalah = ", bil2)
